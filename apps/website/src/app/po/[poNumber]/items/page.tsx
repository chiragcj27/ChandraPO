// Mock data for fallback
async function getMockPO(poNumber: string): Promise<PurchaseOrder | null> {
  try {
    const response = await fetch("/test_PO_response.json");
    const data: PurchaseOrder[] = await response.json();
    const po = data.find((p) => p.PONumber === poNumber);
    if (po) {
      if (!po.PO || po.PO.length === 0) {
        po.PO = ["PO-108248-Uneek.pdf"];
      }
      return po;
    }
    return null;
  } catch (error) {
    console.error("Error loading mock data:", error);
    return null;
  }
}

